import { useRef, useState } from "react";
import { Box, HStack } from "@chakra-ui/react";
import { Editor } from "@monaco-editor/react";
import LanguageSelector from "./LangSelector";
import { CODE_SNIPPETS } from "./Languages";
import Output from "./Output";

const CodeEditor = () => {
  const editorRef = useRef();
  const [value, setValue] = useState("");
  const [language, setLanguage] = useState("javascript");

  const onMount = (editor) => {
    editorRef.current = editor;
    editor.focus();
  };

  const onSelect = (language) => {
    setLanguage(language);
    setValue(CODE_SNIPPETS[language]); // Replace with actual code snippet fetching logic or API call. For now, it's static.  // TODO: Add logic to fetch code snippet from server or API.  // TODO: Handle error case when fetching code snippet fails.  // TODO: Add support for multiple code snippets and display them accordingly.  // TODO: Add support for code snippet previews or execution.  // TODO: Add support for syntax highlighting and formatting.
  };

  return (
    <Box>
      <HStack spacing={4}>
        <Box w="50%">
          <LanguageSelector language={language} onSelect={onSelect} />
          <Editor
            options={{
              minimap: {
                enabled: false,
              },
            }}
            height="75vh"
            theme="vs-light"
            language={language}
            defaultValue={language}
            onMount={onMount}
            value={value}
            onChange={(value) => setValue(value)}
          />
        </Box>
        <Output editorRef={editorRef} language={language} />
      </HStack>
    </Box>
  );
};
export default CodeEditor;